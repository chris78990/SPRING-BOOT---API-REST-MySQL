DROP TABLE IF EXISTS STUDENT;
DROP TABLE IF EXISTS EXAM;
DROP TABLE IF EXISTS COURSE;

CREATE TABLE STUDENT (
	ID VARCHAR(250) PRIMARY KEY,
	firstname VARCHAR(250),
	lastname VARCHAR(250),
	age INT
);

CREATE TABLE EXAM (
	ID VARCHAR(250) PRIMARY KEY,
	coef INT,
	date_exam VARCHAR(250)
);

CREATE TABLE COURSE (
	ID VARCHAR(250) PRIMARY KEY,
	name VARCHAR(250)
);

